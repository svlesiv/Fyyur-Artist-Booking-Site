def is_valid(data_to_check, valid_data):
    for value in data_to_check:
        if value not in valid_data:
            raise ValidationError(
                'Invalid value, must be one of: {}'.format(
                    " ".join(valid_data)))


class BaseForm(Form):
    def is_genres_valid_check(form, field):
        is_valid(field.data, list_of_genres)


